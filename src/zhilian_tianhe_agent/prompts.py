def report_prompt(all_results: dict) -> str:
    return f"""
请把以下模块结果整合成一份“智链天河企业运营服务报告”。
要求：
1. 结构完整，适合导出给园区企业服务中心、商圈运营方或中小企业使用。
2. 必须使用 Markdown 二级标题“##”分模块输出。
3. 不要重复堆砌，提炼成一份正式报告。
4. 若存在“实施计划”内容，应优先提炼 SOP、成效指标、数据安全边界和推广路径。
5. 保留必要风险声明。

建议结构：
## 一句话结论
## 企业背景与主要需求
## 关键事项汇总
## 合同与合作风险
## 政策与材料准备
## 供需协作方案
## 实施计划与指标
## 风险声明

【模块结果】
{all_results}
""".strip()
